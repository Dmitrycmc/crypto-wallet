import {fromUsdtoWei} from './converter';

const decimals = '4';

const resultsMap = {
    '0': '0',
    '1': '0.0001',
    '10': '0.001',
    '12345678': '1234.5678',
    '12345600': '1234.56',
    '12340000': '1234',
    '12000000': '1200',
    '12000056': '1200.0056',
    '120000560': '12000.056',
    // > Number.MAX_SAFE_INTEGER
    '90071992547409911234': '9007199254740991.1234',
    '9007199254740991123400': '900719925474099112.34',
    '900719925474099112340000': '90071992547409911234',
    '90071992547409911234000000': '9007199254740991123400',
    '90071992547409911234000056': '9007199254740991123400.0056',
    '900719925474099112340000560': '90071992547409911234000.056',
};

describe('fromUsdtoWei', () => {
    Object.entries(resultsMap).forEach(([input, output]) => {
        test(`fromUsdtWei(${input}, 4) is ${output}`, () => {
            expect(fromUsdtoWei(input, decimals)).toBe(output);
        });
    });
});